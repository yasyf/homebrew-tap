# Homebrew cask for the cookiesync CLI, shipped inside CookieSync.app.
#
# The CLI is wrapped in a Developer ID signed, notarized, and stapled .app so its
# macOS TCC grants (kTCCServiceSystemPolicyAppBundles + Automation) are keyed to the
# bundle's CFBundleIdentifier (client_type=0) and SURVIVE upgrades — a bare Cellar
# Mach-O is client_type=1, keyed by its versioned path, so every release mints a
# fresh TCC client and re-prompts the user. tccd attributes a nested binary to its
# enclosing bundle, so the `binary` symlink into Contents/MacOS/cookiesync carries
# that durable identity to every exec: the interactive CLI AND the synckit-driven
# resident helper LaunchAgent (com.github.yasyf.synckit.helper.cookiesync), whose
# plist Program is this same /opt/homebrew/bin/cookiesync symlink.
#
# The whole .app must stay intact — splitting it apart severs the bundle identity the
# grant is keyed to — so the cask stages the bundle in place and only symlinks the
# inner CLI; it never moves the .app into /Applications.
#
# This file is GENERATED by cookiesync's release.yml app-build job and pushed here on
# every tagged release — do not hand-edit; change the template at cookiesync's
# .github/cask/cookiesync.rb.tmpl instead.
cask "cookiesync" do
  version "0.21.0"
  sha256 "685e8c3290ecb1816c5292bc004b59d12c5aa2184d9eaa53db624a26374c2f2a" # app

  url "https://github.com/yasyf/cookiesync/releases/download/v#{version}/CookieSync-v#{version}-darwin.zip"
  name "cookiesync"
  desc "Sync your browser cookies across your Macs"
  homepage "https://github.com/yasyf/cookiesync"

  # The signed Secure-Enclave helper ships as the authkit cask (published from the
  # authkit repo). Installing cookiesync pulls it so the Touch ID-gated vault and the
  # per-boot Enclave key cache are available before the first sync.
  depends_on cask: "authkit"

  # Symlink the CLI to the bundle-inner binary (never a bare move): the enclosing
  # CookieSync.app stays staged in the Caskroom, so exec resolves through the symlink
  # into the bundle and tccd keys the grant to the bundle identifier.
  binary "CookieSync.app/Contents/MacOS/cookiesync"

  # The .app is notarized + stapled, so Gatekeeper clears it offline; the recursive
  # quarantine strip on the bundle keeps a first exec friction-free even if the staple
  # check is skipped.
  #
  # brew upgrade swaps the staged bundle on disk but never touches launchd, so the
  # resident helper (com.github.yasyf.synckit.helper.cookiesync) keeps serving the old
  # dispatcher until reboot. Kick it after every (re)install so upgrades self-deploy.
  # must_succeed: false keeps first install a no-op — the agent only exists after
  # `cookiesync install` + `synckitd install`.
  postflight do
    system_command "/usr/bin/xattr",
                   args:         ["-dr", "com.apple.quarantine", "#{staged_path}/CookieSync.app"],
                   must_succeed: false
    system_command "/bin/launchctl",
                   args:         ["kickstart", "-k", "gui/#{Process.uid}/com.github.yasyf.synckit.helper.cookiesync"],
                   must_succeed: false,
                   print_stderr: false
  end

  caveats <<~EOS
    Wire up the watch daemon and reconcile tick, then track your first browser:

      cookiesync install
  EOS
end
